<!--

               Artirma ve Azaltma Operatorleri


1-      ++ ve --
2-      ++$a      Onceden artirma
3-      $a++      Sonradan artirma
4-      --$a      Onceden eksiltme
5-      $a--      Sonradan eksiltme
-->

<?php

$a = 3;
echo $a++;     //cevap 3 olur  cunku sonradan artirma operatoru ilk once degiskeni yazar sonra sayiyi atirir yani bi dongu yasanir oncesinde!
echo "<br>";

$b = 3;
echo ++$b;  // cevap 4 olur onceden artirma operatoru burda!!

// Eksininde mantigi ayni !!




?>
